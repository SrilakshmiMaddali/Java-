public class Switch {
    public static void main(String a[]){
        int i =1;
        switch(i)
        {
            case 0:
                System.out.println("Zero");
                break;
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 6:
                System.out.println("Six");
                break;
            case 8:
                System.out.println("Eight");
                break;
            case 9:
                System.out.println("Nine");
                break;
            default:
                System.out.println("None");
                
        }
    }
    
}
